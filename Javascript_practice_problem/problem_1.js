var result = 91;

if(result >= 0 && result < 40){
    console.log("F GRADE");
}
else if(result >= 40 && result < 60){
    console.log("D Grade");
}
else if(result >= 60 && result < 70){
    console.log("C Grade");
}
else if(result >= 70 && result < 80){
    console.log("B Grade");
}
else if(result >= 80 && result < 90){
    console.log("A Grade");
}
else{
    console.log("A+");
}
